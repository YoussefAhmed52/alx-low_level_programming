#include "3-calc.h"
/**
 * op_add - adds two inegers
 * @a: first int
 * @b: second int
 *
 * Return: a + b
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtract two inegers
 * @a: first int
 * @b: second int
 *
 * Return: a - b
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiply two inegers
 * @a: first int
 * @b: second int
 *
 * Return: multiplication
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divides two inegers
 * @a: first int
 * @b: second int
 *
 * Return: quotiant
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - divides two inegers
 * @a: first int
 * @b: second int
 *
 * Return: reminder
 */
int op_mod(int a, int b)
{
	return (a % b);
}
